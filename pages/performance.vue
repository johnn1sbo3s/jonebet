  const zoomPlugin = (await import("chartjs-plugin-zoom")).default;
  const annotationPlugin = (await import("chartjs-plugin-annotation")).default;
  Chart.register(zoomPlugin);
  Chart.register(annotationPlugin);
}

Chart.register(...(registerables || []));

const chartData = ref({
  labels: [],
  datasets: [
    {
      label: "Acúmulo de capital",
      data: [],
      borderColor: "rgb(74 222 128)",
      backgroundColor: "rgb(74 222 128)",
      pointRadius: 1,
      pointHoverRadius: 7,
      fill: false,
      tension: 0.2,
    },
  ],
});

const lineChartProps = {
  width: 400,
  height: 300,
};

const chartOptions = ref({
  responsive: true,
  maintainAspectRatio: true,
  scales: {
    y: {
      beginAtZero: false,
      ticks: {
        callback: (value) => {
          return value.toFixed(1);
        },
      },
    },
    x: {
      beginAtZero: false,
      ticks: {
        callback: (value) => {
          return value.toFixed(1);
        },
      },
    },
  },
  plugins: {
    legend: {
      position: "top",
      display: true,
    },
    zoom: {
      zoom: {
        wheel: {
          enabled: true,
        },
        pinch: {
          enabled: true,
        },
        mode: "x",
        drag: {
          enabled: true,
          borderColor: "rgb(54, 162, 235)",
          borderWidth: 1,
          backgroundColor: "rgba(54, 162, 235, 0.3)",
        },
      },
      pan: {
        enabled: true,
        mode: "x",
        modifierKey: "ctrl",
      },
    },
    annotation: {
      annotations: {
        line1: {
          type: "line",
          xMin: -100,
          xMax: -100,
          borderColor: "rgb(20 184 166)",
          borderWidth: 2,
        },
      },
    },
  },
});

const realData = ref({});
const valData = ref({});
const totalData = ref({});
const showVal = ref(true);
const listModels = ref([]);
const betsData = ref({});
const objectModel = ref({});
const modelPerformanceData = ref({});
const chartKey = ref(0);

const fetchData = async (url) => {
  try {
    const req = await fetch(url);
    const data = await req.json();
    return data;
  } catch (error) {
    console.error("Erro ao buscar os dados:", error);
    return [];
  }
};

const changeShowVal = () => {
  showVal.value = !showVal.value;
};

// Pega os dados da API
const performanceData = await fetchData(
  "http://localhost:5000/model_performance"
);
betsData.value = await fetchData("http://localhost:5000/model_bets");

modelPerformanceData.value = performanceData.value;

Object.values(performanceData).forEach((item) => {
  let name = item.modelo;
  name = name.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
  if (!listModels.value.includes(name)) {
    listModels.value.push(name);
  }
});

const chosenModel = ref(listModels.value[0]);

const changeModel = () => {
  Object.values(performanceData).forEach((item) => {
    let name = item.modelo;
    name = name.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
    if (name === chosenModel.value) {
      objectModel.value = item;
      const { real } = objectModel.value;
      const { val } = objectModel.value;
      const { total } = objectModel.value;
      realData.value = real;
      valData.value = val;
      totalData.value = total;
    }
  });
};

const getBetsArray = (showValidation = true) => {
  let nRange = valData.value.entradas;
  let betsTotal = totalData.value.pl_history;
  const betsReal = ref(betsTotal.slice(nRange));
  if (showValidation) {
    chartOptions.value.plugins.annotation.annotations.line1.xMax = nRange;
    chartOptions.value.plugins.annotation.annotations.line1.xMin = nRange;
    cumulativeSum(betsTotal);
  } else {
    chartOptions.value.plugins.annotation.annotations.line1.xMax = -100;
    chartOptions.value.plugins.annotation.annotations.line1.xMin = -100;
    cumulativeSum(betsReal.value);
  }
};

function cumulativeSum(array) {
  if (array.length === 0) {
    return [];
  }
  let cumSum = [array[0]];
  let listIndex = [];
  for (let i = 1; i < array.length; i++) {
    cumSum.push(cumSum[i - 1] + array[i]);
  }
  for (let i = 1; i < array.length + 10; i++) {
    listIndex.push(i);
  }
  chartData.value.labels = listIndex;
  chartData.value.datasets[0].data = cumSum;
}

const changeChartData = () => {
  getBetsArray(showVal.value);
};

const buildInfo = () => {
  changeModel();
  changeChartData();
  chartKey.value++;
};

watchEffect(() => {
  buildInfo();
});
